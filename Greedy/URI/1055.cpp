#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main(){
    int t, n;
    vector<int> d;
    scanf("%d", &t);
    for(int ti = 1; ti <= t ; ++ti){
        scanf("%d", n);
        d.assign(n,0);
        for(int& x : d) scanf("%d", &x);
    }

    return 0;
}